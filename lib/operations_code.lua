local Class = {}

-- code of each node
--  {
--    ["operation id"] = $number,
--  }
local codes = {
  ["CALLID"]            = 01,
  ["GOTO"]              = 02,
  ["IFFALSEGOTO"]       = 03,
  ["IFGOTO"]            = 04,
  ["LABEL"]             = 05,
  ["PARAM"]             = 06,
  ["RET_OP"]            = 07,
  ["RET_NIL"]           = 08,
  
  
  ["ID=rval"]           = 10,
  ["ID=BYTErval"]       = 11,
  ["ID=ID[rval]"]       = 12,
  ["ID=BYTEID[rval]"]   = 13,
  ["ID=-rval"]          = 14,
  ["ID=NEWrval"]        = 15,
  ["ID=NEWBYTErval"]    = 16,
  ["ID=rvalEQrval"]     = 17,
  ["ID=rvalNErval"]     = 18,
  ["ID=rvalGErval"]     = 19,
  ["ID=rvalLErval"]     = 20,
  ["ID=rval<rval"]      = 21,
  ["ID=rval>rval"]      = 22,
  ["ID=rval+rval"]      = 23,
  ["ID=rval-rval"]      = 24,
  ["ID=rval*rval"]      = 25,
  ["ID=rval/rval"]      = 26,
  ["ID[rval]=rval"]     = 27,
  ["ID[rval]=BYTErval"] = 28,
}


--==============================================================================
-- Public Methods
--==============================================================================

function Class.GetList ()
  return codes
end

function Class.GetName (operation_code)
  assert(type(operation_code) == "number")
  for name, code in pairs(codes) do
    if (code == operation_code) then
      return name
    end
  end
  return nil
end


--==============================================================================
-- Private Methods
--==============================================================================



--==============================================================================
-- Return
--==============================================================================

return Class