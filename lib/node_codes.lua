local Nodes = {}

-- code of each node
--  {
--    ["node id"] = $number,
--  }
local codes = {
  ["ATTRIBUTION"] = 01,
  ["CALL"]        = 02,
  ["DECLARE"]     = 03,
  ["ELSEIF"]      = 04,
  ["FUNCTION"]    = 05,
  ["IF"]          = 06,
  ["NEGATE"]      = 07,
  ["NEWVAR"]      = 08,
  ["OPERATOR"]    = 09,
  ["PARAMETER"]   = 10,
  ["PARENTHESIS"] = 11,
  ["PROGRAM"]     = 12,
  ["RETURN"]      = 13,
  ["UNARY"]       = 14,
  ["VALUE"]       = 15,
  ["VAR"]         = 16,
  ["WHILE"]       = 17,
}


--==============================================================================
-- Public Methods
--==============================================================================

function Nodes.GetNodesList ()
  return codes
end

function Nodes.GetNodeName (node_code)
  assert(type(node_code) == "number")
  for name, code in pairs(codes) do
    if (code == node_code) then
      return name
    end
  end
  return nil
end


--==============================================================================
-- Private Methods
--==============================================================================



--==============================================================================
-- Return
--==============================================================================

return Nodes